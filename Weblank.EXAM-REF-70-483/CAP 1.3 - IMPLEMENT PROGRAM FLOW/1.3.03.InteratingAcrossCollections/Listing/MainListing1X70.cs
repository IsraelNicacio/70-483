using System;
using System.Threading;
using System.Threading.Tasks;

namespace _10.InteratingAcrossCollections
{
    // While Loop ############################
    // Within a while or do-while loop, 
    // you can use the continue and break statements just as with a for loop.
    public class MainListing1X70
    {
        public MainListing1X70()
        {
            do
            {
                Console.WriteLine("Executed once!");
            }
            while (false);
            {
            }
            Console.ReadLine();
        }
    }
}
